package feb.statementAndLoops.forLoop;

public class ForLoopEx2 {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            if (i == 5) {
                System.out.println("Five");
            }else {
                System.out.println(i);
            }
        }
    }
}
