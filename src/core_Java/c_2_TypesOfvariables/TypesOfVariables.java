package core_Java.c_2_TypesOfvariables;

public class TypesOfVariables {
    public static void main(String[] args) {

        // Variable is storage or container which holds the data
        // Variable values ca be changed during the program execution

        /*There are three types of variables based on Scope
        1. Local Variables
        2. Static Variables
        3. Instance Variables
        Note : There is no concept of global variables in java

        There are two types variables based on data types
         1. Primitive variables -holds primitive data type values
         2. Reference variables -holds reference (or address) to objects rather than the actual data

*/
        System.out.println("There are three types of variables");
        System.out.println("1. Local Variables");
        System.out.println("2. Static Variables");
        System.out.println("3. Instance Variables");
        System.out.println();

        System.out.println("There is no concept of global variables in java");
    }
}
